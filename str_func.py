def str_func_upper(text: str):
    """которая принимает на вход строку и возвращает ее со всеми заглавными буквами"""
    return text.upper()


def str_func_title(text: str):
    """которая делает заглавными первые буквы каждого слова в строке, поступившей на вход функции"""
    return text.title()
